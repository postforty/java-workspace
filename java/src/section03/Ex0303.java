package section03;

public class Ex0303 {
    public static void main(String[] args) {
        char a = 'a';

        // 문자는 증감 연산자 사용 가능
        System.out.println("a\t: " + a);
        System.out.println("++a\t: " + ++a);
        System.out.println("++a\t: " + ++a);
        System.out.println("(int) a\t: " + (int) a);
    }
}
