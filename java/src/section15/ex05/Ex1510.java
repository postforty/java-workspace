package section15.ex05;

public class Ex1510 {
    public static void main(String[] args) {
        int absValue = Math.abs(-10);
        System.out.println("absValue: " + absValue);

        double powerValue = Math.pow(2, 3);
        System.out.println("powerValue: " + powerValue);

        double sqrtValue = Math.sqrt(16);
        System.out.println("sqrtValue: " + sqrtValue);

        int maxValue = Math.max(10, 20);
        System.out.println("maxValue: " + maxValue);

        int minValue = Math.min(10, 20);
        System.out.println("minValue: " + minValue);

        double radians = Math.toRadians(30);
        double sinValue = Math.sin(radians);
        System.out.println("sinValue: " + sinValue);

        double logValue = Math.log(10);
        System.out.println("logValue: " + logValue);
    }
}
