package section03;

public class Ex0313 {
  public static void main(String[] args) {
    int num1 = 1;
    int num2 = 2;

    // 관계(비교) 연산자
    // - true/false로 평가

    System.out.println(num1 > num2); // false
    System.out.println(num1 < num2); // true
    System.out.println(num1 >= num2); // false
    System.out.println(num1 <= num2); // true
    System.out.println(num1++ >= num2); // false
    System.out.println(++num1 >= num2); // true
    // System.out.println(num1 == num2); // false
    // System.out.println(num1 != num2); // true

  }
}
